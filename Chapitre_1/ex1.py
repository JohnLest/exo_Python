# Écrire un script qui permet d'afficher les 10 premiers termes (avec le calcul) 
# de la table de multiplication par 3.

i = 1 # On initialise i à 1
while (i <= 10): # Temps que i est plus petit ou égale à 10 
    print (f"{i} * 3 = {i*3}") # on écrit à l'ecran tout en faisant le calcul de i*3
    i +=1 # On incrémente i de 1
